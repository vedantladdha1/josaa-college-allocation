export const quotas = ["AI", "HS", "OS", "SC", "ST", "OBC-NCL", "EWS", "OPEN"]

export const rounds = ["Round 1", "Round 2", "Round 3", "Round 4", "Round 5", "Round 6"]

export type Institute = (typeof institutes)[number]
export type Program = (typeof programs)[number]
export type Quota = (typeof quotas)[number]
export type Round = (typeof rounds)[number]

export interface Seat {
  instituteCode: string
  programCode: string
  category: Quota
  openingRank: number
  closingRank: number
}

export const seatMatrix: Seat[] = institutes.flatMap((institute) =>
  programs.flatMap((program) =>
    quotas.map((quota) => ({
      instituteCode: institute.code,
      programCode: program.code,
      category: quota,
      openingRank: Math.floor(Math.random() * 1000) + 1,
      closingRank: Math.floor(Math.random() * 4000) + 1000,
    })),
  ),
)

// Ensure closing rank is always higher than opening rank
seatMatrix.forEach((seat) => {
  if (seat.closingRank < seat.openingRank) {
    ;[seat.openingRank, seat.closingRank] = [seat.closingRank, seat.openingRank]
  }
})

